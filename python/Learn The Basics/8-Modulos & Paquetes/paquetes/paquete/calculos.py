def suma(val_A, val_B):
    return val_A + val_B

def resta(val_A, val_B):
    return val_A - val_B

def multiplicacion(val_A, val_B):
    return val_A * val_B

def division(val_A, val_B):
    return val_A / val_B

def potencia(val_A, val_B):
    return val_A ** val_B